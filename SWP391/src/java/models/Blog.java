package models;

/**
 *
 * @author DELL
 */
public class Blog {
    
    String author;
    String image;
    String tittle;
    String small;
    String big;

    public Blog() {
    }

    public Blog(String image,String author, String tittle, String small, String big) {
        this.author = author;
        this.image = image;
        this.tittle = tittle;
        this.small = small;
        this.big = big;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTittle() {
        return tittle;
    }

    public void setTittle(String tittle) {
        this.tittle = tittle;
    }

    public String getSmall() {
        return small;
    }

    public void setSmall(String small) {
        this.small = small;
    }

    public String getBig() {
        return big;
    }

    public void setBig(String big) {
        this.big = big;
    }
    
    
    
}
